C  PROGRAM PENTRU SIMULAREA FUNCTIONARII UNUI SUMATOR BINAR
      LOGICAL X1,X2,X3,S,T
      READ(*,*) X1,X2,X3
C   1 FORMAT(3L4)
      T=X1.AND.X2.OR.X2.AND.X3.OR.X1.AND.X3
      S=X1.AND.X2.AND.X3.OR.(X1.OR.X2.OR.X3).AND..NOT.T 
      WRITE(*,2) X1,X2,X3,T,S 
    2 FORMAT(/////10X,3L8,2L10/////)
      STOP
      END
